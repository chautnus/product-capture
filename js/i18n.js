let currentLang = 'en';

function setCurrentLang(lang) {
    currentLang = lang;
}

function t(key) {
    return translations[currentLang][key] || key;
}

function updateTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (el.tagName === 'INPUT') {
            el.placeholder = t(key);
        } else {
            el.textContent = t(key);
        }
    });
    // Note: Instead of direct imports which cause circular deps, we can dispatch an event or assume they are globably available.
    // However, since we are doing ES modules, we should import them. Let's comment them out and let the caller trigger re-renders.
    // renderCategories();
    // renderDynamicForm();
    // renderProducts();
    
    // Better way: dispatch event
    window.dispatchEvent(new Event('languageChanged'));
}
